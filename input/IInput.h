#define MIK_MID				1
#define MIK_RIGHT			2
class InputEventDevice
{
private:
	vector<my_ptr_function_class_2<int, int, int> > mouseMoveCallback;

public:
	virtual ~InputEventDevice() {}
	virtual bool checkKeyIsDown(int key) = 0;
	virtual bool checkMouseKeyIsDown(int mouseKey) = 0;
	virtual bool updateState() = 0;
	virtual vec2 getMousePosition() = 0;

	void onMouseMove(int dx, int dy)
	{
		for (auto f : mouseMoveCallback)
		{
			f(dx, dy);
		}
	}

	void registerMouseMoveCallback(int(*f)(int, int))
	{
		this->registerMouseMoveCallback(my_ptr_function(f));
	}

	void registerMouseMoveCallback(my_ptr_function_class_2<int, int, int>& f)
	{
		mouseMoveCallback.push_back(f);
	}
};

